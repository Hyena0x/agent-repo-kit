#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

function parseArgs(argv) {
  const options = {
    dryRun: false,
    root: process.cwd()
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === "--dry-run") {
      options.dryRun = true;
      continue;
    }

    if (arg === "--root") {
      options.root = argv[index + 1];
      index += 1;
    }
  }

  return {
    dryRun: options.dryRun,
    root: path.resolve(options.root)
  };
}

function readText(root, relativePath) {
  try {
    return readFileSync(path.join(root, relativePath), "utf8");
  } catch {
    return "";
  }
}

function readJson(root, relativePath) {
  try {
    return JSON.parse(readText(root, relativePath));
  } catch {
    return null;
  }
}

function detectVerificationCommands(root) {
  const packageJson = readJson(root, "package.json");
  const scripts = packageJson?.scripts ?? {};
  const commands = [];

  if (scripts.test) {
    commands.push("npm test");
  }

  if (scripts.build) {
    commands.push("npm run build");
  }

  if (scripts.lint) {
    commands.push("npm run lint");
  }

  if (existsSync(path.join(root, "Cargo.toml"))) {
    commands.push("cargo test");
  }

  return commands;
}

function renderAgentsMd(root) {
  const commands = detectVerificationCommands(root);
  const verificationSection =
    commands.length > 0
      ? commands.map((command) => `- \`${command}\``).join("\n")
      : "- Add the main test, build, or lint command here before relying on agent changes.";

  return `# AGENTS.md

Generated by Agent Repo Kit.

## Project Overview

This repository should be easy for AI coding agents to inspect, edit, and verify.

## Verification

Run the strongest relevant checks before claiming a change works:

${verificationSection}

## Safety

- Do not read or print secrets from .env files, local credentials, certificates, or private keys.
- Ask before destructive Git commands, force pushes, package publishes, or broad file deletion.
- Keep changes focused on the requested task and summarize what was verified.
`;
}

function ensureIgnoreLines(current) {
  const desired = [".env", ".env.*", "*.pem", ".DS_Store"];
  const lines = current.split(/\r?\n/).filter((line, index, all) => line.length > 0 || index < all.length - 1);
  const present = new Set(lines);

  for (const line of desired) {
    if (!present.has(line)) {
      lines.push(line);
    }
  }

  return `${lines.join("\n").replace(/\n+$/, "")}\n`;
}

function planChanges(root) {
  const changes = [];

  if (!existsSync(path.join(root, "AGENTS.md"))) {
    changes.push({
      path: "AGENTS.md",
      content: renderAgentsMd(root),
      reason: "add a clear agent instruction entrypoint"
    });
  }

  const gitignore = readText(root, ".gitignore");
  const nextGitignore = ensureIgnoreLines(gitignore);

  if (gitignore !== nextGitignore) {
    changes.push({
      path: ".gitignore",
      content: nextGitignore,
      reason: "make local secret and machine files explicit"
    });
  }

  return changes;
}

function writeChange(root, change) {
  const absolutePath = path.join(root, change.path);
  mkdirSync(path.dirname(absolutePath), { recursive: true });
  writeFileSync(absolutePath, change.content);
}

const options = parseArgs(process.argv.slice(2));
const changes = planChanges(options.root);

if (options.dryRun) {
  console.log("Agent Repo Fix DRY RUN");
} else {
  console.log("Agent Repo Fix");
}

if (changes.length === 0) {
  console.log("No changes needed.");
  process.exit(0);
}

for (const change of changes) {
  console.log(`- ${options.dryRun ? "Would write" : "Writing"} ${change.path}: ${change.reason}`);

  if (!options.dryRun) {
    writeChange(options.root, change);
  }
}

if (!options.dryRun) {
  console.log(`Applied ${changes.length} change${changes.length === 1 ? "" : "s"}.`);
}
