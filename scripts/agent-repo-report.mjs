#!/usr/bin/env node

import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const PRODUCT = "Agent Repo Kit";
const SLUG = "agent-repo-kit";

function parseArgs(argv) {
  const options = {
    root: process.cwd()
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === "--root") {
      options.root = argv[index + 1];
      index += 1;
    }
  }

  return {
    root: path.resolve(options.root)
  };
}

function fileExists(root, relativePath) {
  return existsSync(path.join(root, relativePath));
}

function readText(root, relativePath) {
  try {
    return readFileSync(path.join(root, relativePath), "utf8");
  } catch {
    return "";
  }
}

function readJson(root, relativePath) {
  try {
    return JSON.parse(readText(root, relativePath));
  } catch {
    return null;
  }
}

function hasWorkflow(root) {
  const workflowDirectory = path.join(root, ".github/workflows");

  try {
    return readdirSync(workflowDirectory).some((entry) => /\.ya?ml$/i.test(entry));
  } catch {
    return false;
  }
}

function hasCursorRules(root) {
  const rulesDirectory = path.join(root, ".cursor/rules");

  try {
    return readdirSync(rulesDirectory).some((entry) => entry.endsWith(".mdc"));
  } catch {
    return false;
  }
}

function detectVerificationCommands(packageJson) {
  const scripts = packageJson?.scripts ?? {};
  const commands = [];

  if (scripts.test) {
    commands.push("npm test");
  }

  if (scripts.build) {
    commands.push("npm run build");
  }

  if (scripts.lint) {
    commands.push("npm run lint");
  }

  return commands;
}

function evaluate(root) {
  const packageJson = readJson(root, "package.json");
  const readme = readText(root, "README.md");
  const gitignore = readText(root, ".gitignore");
  const verificationCommands = detectVerificationCommands(packageJson);

  const checks = [
    {
      id: "agent-instructions",
      title: "Agent instructions",
      weight: 20,
      passed:
        fileExists(root, "AGENTS.md") ||
        fileExists(root, "CLAUDE.md") ||
        hasCursorRules(root),
      detail: "Agents need one obvious place to find repo-specific rules.",
      fix: "Add AGENTS.md and keep tool-specific rules generated from the same policy."
    },
    {
      id: "verification-entrypoint",
      title: "Verification entrypoint",
      weight: 20,
      passed: verificationCommands.length > 0 || fileExists(root, "Cargo.toml"),
      detail: "Agents need a command they can run before claiming a change works.",
      fix: "Expose test, build, or lint commands in package scripts or project docs."
    },
    {
      id: "readme-quickstart",
      title: "README quickstart",
      weight: 15,
      passed: /install|quick start|quickstart|development|test|build/i.test(readme),
      detail: "Agents should not infer setup from scattered files.",
      fix: "Add install, development, and verification commands near the top of README.md."
    },
    {
      id: "ci-workflow",
      title: "CI workflow",
      weight: 15,
      passed: hasWorkflow(root),
      detail: "A remote check gives agents and maintainers a second source of truth.",
      fix: "Add a GitHub Actions workflow for the primary verification command."
    },
    {
      id: "secret-hygiene",
      title: "Secret hygiene",
      weight: 15,
      passed: /^\.env$/m.test(gitignore) && /^\.env\.\*$/m.test(gitignore),
      detail: "Agents need visible boundaries around local secrets.",
      fix: "Ignore .env and .env.* files and keep sample values in .env.example."
    },
    {
      id: "publish-safety",
      title: "Publish safety",
      weight: 15,
      passed:
        !packageJson ||
        Boolean(packageJson.files) ||
        Boolean(packageJson.scripts?.["audit:pack"]),
      detail: "Publishable packages should define what ships.",
      fix: "Use package.json files and an npm-pack audit before release."
    }
  ];

  const score = checks.reduce((total, check) => total + (check.passed ? check.weight : 0), 0);

  return {
    product: PRODUCT,
    slug: SLUG,
    score,
    maxScore: 100,
    generatedAt: new Date().toISOString(),
    root,
    checks,
    verificationCommands
  };
}

function renderMarkdown(report) {
  const rows = report.checks
    .map((check) => {
      const status = check.passed ? "PASS" : "FIX";
      return `| ${status} | ${check.title} | ${check.weight} | ${check.detail} | ${check.fix} |`;
    })
    .join("\n");

  const topFixes = report.checks
    .filter((check) => !check.passed)
    .slice(0, 3)
    .map((check) => `- **${check.title}:** ${check.fix}`);

  return `# Agent Repo Report

Generated by **${PRODUCT}**.

## Score

**${report.score}/${report.maxScore}**

## Checks

| Status | Check | Points | Why it matters | Suggested fix |
| --- | ---: | ---: | --- | --- |
${rows}

## Top fixes

${topFixes.length > 0 ? topFixes.join("\n") : "- No blocking fixes found in this pass."}

## Share card

![Agent-ready score](./agent-repo-card.svg)

## Verification commands detected

${report.verificationCommands.length > 0 ? report.verificationCommands.map((command) => `- \`${command}\``).join("\n") : "- No package verification command detected."}
`;
}

function renderSvg(report) {
  const color = report.score >= 80 ? "#15803d" : report.score >= 60 ? "#b45309" : "#b91c1c";

  return `<svg xmlns="http://www.w3.org/2000/svg" width="920" height="360" viewBox="0 0 920 360" role="img" aria-label="Agent-ready score ${report.score} out of 100">
  <rect width="920" height="360" rx="24" fill="#0f172a"/>
  <text x="48" y="78" fill="#f8fafc" font-family="Arial, sans-serif" font-size="34" font-weight="700">${PRODUCT}</text>
  <text x="48" y="122" fill="#cbd5e1" font-family="Arial, sans-serif" font-size="22">Agent-ready score</text>
  <text x="48" y="238" fill="${color}" font-family="Arial, sans-serif" font-size="112" font-weight="700">${report.score}</text>
  <text x="212" y="238" fill="#94a3b8" font-family="Arial, sans-serif" font-size="48">/100</text>
  <text x="48" y="300" fill="#e2e8f0" font-family="Arial, sans-serif" font-size="22">Make your GitHub repo easier, safer, and more verifiable for AI coding agents.</text>
</svg>
`;
}

function writeOutputs(root, report) {
  mkdirSync(path.join(root, ".agent-repo-kit"), { recursive: true });
  writeFileSync(path.join(root, ".agent-repo-kit/report.json"), `${JSON.stringify(report, null, 2)}\n`);
  writeFileSync(path.join(root, "AGENT_REPO_REPORT.md"), renderMarkdown(report));
  writeFileSync(path.join(root, "agent-repo-card.svg"), renderSvg(report));
}

const options = parseArgs(process.argv.slice(2));
const report = evaluate(options.root);

writeOutputs(options.root, report);

console.log("Agent Repo Report");
console.log(`Score: ${report.score}/${report.maxScore}`);
console.log("Wrote: AGENT_REPO_REPORT.md");
console.log("Wrote: .agent-repo-kit/report.json");
console.log("Wrote: agent-repo-card.svg");
